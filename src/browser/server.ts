/** Serve an HTML response with 200 OK. */
export function serveHtml(res: ServerResponse, html: string): void {
  res.writeHead(200, { "Content-Type": "text/html; charset=utf-8" });
  res.end(html);
}

/**
 * Read a JSON POST body (≤1 MB), validate headers + CSRF + a zod schema,
 * then invoke `onValid`. Each failure responds with the appropriate status.
 */
export function readJsonWithCsrf<S extends z.ZodType, R>(
  req: IncomingMessage,
  res: ServerResponse,
  ctx: FlowContext<R>,
  schema: S,
  onValid: (data: z.infer<S>) => void | Promise<void>,
): void {
  const headerCheck = validateLoopbackPostHeaders(req, {
    allowedHosts: ctx.allowedHosts as string[],
  });
  if (!headerCheck.ok) {
    res.writeHead(headerCheck.status, { "Content-Type": "text/plain" });
    res.end(headerCheck.message);
    return;
  }

  let body = "";
  let size = 0;
  req.on("data", (chunk: Buffer) => {
    size += chunk.length;
    if (size > MAX_BODY_SIZE) {
      res.writeHead(413, { "Content-Type": "text/plain" });
      res.end("Payload Too Large");
      req.destroy();
      return;
    }
    body += chunk.toString();
  });
  req.on("end", () => {
    let parsed: unknown;
    try {
      parsed = JSON.parse(body);
    } catch {
      res.writeHead(400, { "Content-Type": "text/plain" });
      res.end("Invalid request body");
      return;
    }

    const parseResult = (schema as z.ZodType).safeParse(parsed);
    if (!parseResult.success) {
      res.writeHead(400, { "Content-Type": "text/plain" });
      res.end("Invalid request body");
      return;
    }

    // Verify CSRF token — it must be present as `csrfToken` in the parsed body
    const data = parseResult.data as Record<string, unknown>;
    if (!verifyCsrfToken(ctx.csrfToken, data["csrfToken"])) {
      res.writeHead(403, { "Content-Type": "text/plain" });
      res.end("Forbidden: invalid CSRF token");
      return;
    }

    void Promise.resolve(onValid(parseResult.data as z.infer<S>)).catch((err: unknown) => {
      if (!res.headersSent) {
        const message = err instanceof Error ? err.message : String(err);
        logger.error("browser flow onValid error", { error: message });
        res.writeHead(500, { "Content-Type": "text/plain" });
        res.end(`Error: ${message}`);
      }
    });
  });
}

/** Write a 200 JSON response then close the server after a 100 ms flush delay. */
export function respondAndClose(res: ServerResponse, server: Server, body: unknown): void {
  res.writeHead(200, { "Content-Type": "application/json" });
  res.end(JSON.stringify(body));
  setTimeout(() => {
    server.close();
  }, 100);
}
